#include "libft.h"

void	ft_putnbr(int nb)
{
	long long int a;

	a = nb;
	if (a < 0)
	{
		a = -a;
		ft_putchar('-');
	}
	if (a >= 10)
		ft_putnbr(a / 10);
	ft_putchar(a % 10 + '0');
}
