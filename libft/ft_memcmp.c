#include <unistd.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*t1;
	unsigned char	*t2;
	size_t			i;

	i = 0;
	t1 = (unsigned char *)s1;
	t2 = (unsigned char *)s2;
	while (i < n && t1[i] == t2[i])
		i++;
	if (i == n)
		return (0);
	return (t1[i] - t2[i]);
}
